static void rx_reset_edid(struct rx_edid *edid)
{
	uint16_t i;
	uint16_t j;
	uint8_t *block = NULL;

	dpu_check_and_no_retval((edid == NULL), err, "edid is NULL!\n");

	for (i = 0; i < MAX_EDID_BLOCK_NUM; i++) {
		block = edid->block[i].payload;
		for (j = 0; j < EDID_BLOCK_SIZE; j++)
			block[j] = 0x0;
	}
}

static void rx_load_default_edid(struct rx_edid *edid, uint32_t timing_code)
{
	uint16_t i;
	uint16_t j;
	uint8_t *block = NULL;
	uint8_t *default_edid;

	dpu_check_and_no_retval((edid == NULL), err, "edid is NULL!\n");
	dpu_check_and_no_retval((edid->valid_block_num >= MAX_EDID_BLOCK_NUM), err, "Invalid block num!\n");

	default_edid = (timing_code == TIMING_1080P) ? edid_1920x1080_8bit : edid_720x480_8bit;

	for (i = 0; i < edid->valid_block_num; i++) {
		block = edid->block[i].payload;
		for (j = 0; j < EDID_BLOCK_SIZE; j++)
			block[j] = default_edid[EDID_BLOCK_SIZE * i + j];
	}
}

void rx_edid_dump(struct rx_edid* edid)
{
	uint16_t i;
	uint16_t j;
	uint8_t *block = NULL;

	dpu_check_and_no_retval((edid == NULL), err, "edid is NULL!\n");
	dpu_check_and_no_retval((edid->valid_block_num >= MAX_EDID_BLOCK_NUM), err, "Invalid block num!\n");

	pr_info("EDID ++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++\n");
	for (i = 0; i < edid->valid_block_num; i++) {
		block = edid->block[i].payload;
		for (j = 0; j < EDID_BLOCK_SIZE;) {
			if (!(j % 16))
				pr_info("EDID[%04x]:%02x %02x %02x %02x %02x %02x %02x %02x %02x %02x %02x %02x %02x %02x %02x %02x\n",
					j, block[j], block[j + 1], block[j + 2], block[j + 3],
					block[j + 4], block[j + 5], block[j + 6], block[j + 7],
					block[j + 8], block[j + 9], block[j + 10], block[j + 11],
					block[j + 12], block[j + 13], block[j + 14], block[j + 15]);
			j += 16;
		}
		if (i != edid->valid_block_num - 1)
			pr_info("<<<-------------------------------------------------------------->>>\n");
	}
	pr_info("EDID ------------------------------------------------------------\n");
}

void rx_edid_init(struct rx_edid *edid, uint8_t block_num, uint32_t timing_code)
{
	dpu_check_and_no_retval((edid == NULL), err, "edid is NULL!\n");
	dpu_check_and_no_retval((block_num >= MAX_EDID_BLOCK_NUM), err, "Invalid block num!\n");

	pr_info("init edid +++\n");
	edid->valid_block_num = block_num;
	rx_reset_edid(edid);
	rx_load_default_edid(edid, timing_code);
	pr_info("init edid ---\n");
}
