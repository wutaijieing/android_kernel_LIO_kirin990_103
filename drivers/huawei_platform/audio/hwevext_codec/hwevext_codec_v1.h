#define HWEVEXT_CODEC_V1_SERDATA2_LEN_24	0x00
#define HWEVEXT_CODEC_V1_SERDATA2_LEN_20	0x04
#define HWEVEXT_CODEC_V1_SERDATA2_LEN_18	0x08
#define HWEVEXT_CODEC_V1_SERDATA2_LEN_16	0x0c
#define HWEVEXT_CODEC_V1_SERDATA2_LEN_32	0x10

/* HWEVEXT_CODEC_V1_GPIO_DEBOUNCE	*/
#define HWEVEXT_CODEC_V1_GPIO_ENABLE_INTERRUPT	0x02

/* HWEVEXT_CODEC_V1_GPIO_FLAG */
#define HWEVEXT_CODEC_V1GPIO_FLAG_GM_NOT_SHORTED 0x02
#define HWEVEXT_CODEC_V1_GPIO_FLAG_HP_INSERTED	0x04

#endif
