#define HWEVEXT_CODEC_V1_ADC_ALC5	0x2d
#define HWEVEXT_CODEC_V1_ADC_ALC_NG	0x2e

/* DAC Control */
#define HWEVEXT_CODEC_V1_DAC_PDN	0x2f
